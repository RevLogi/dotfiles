-- [[ Basic Autocommands ]]
--  See `:help lua-guide-autocommands`

-- Highlight when yanking (copying) text
--  Try it with `yap` in normal mode
--  See `:help vim.hl.on_yank()`
vim.api.nvim_create_autocmd('TextYankPost', {
  desc = 'Highlight when yanking (copying) text',
  group = vim.api.nvim_create_augroup('kickstart-highlight-yank', { clear = true }),
  callback = function()
    vim.hl.on_yank()
  end,
})

-- Set auto-save
local autosave_group = vim.api.nvim_create_augroup('AutoSaveGroup', { clear = true })

vim.api.nvim_create_autocmd({ 'InsertLeave', 'TextChanged' }, {
  group = autosave_group,
  pattern = '*',
  callback = function()
    local buf = vim.api.nvim_get_current_buf()
    local buftype = vim.bo[buf].buftype
    local modifiable = vim.bo[buf].modifiable
    local modified = vim.bo[buf].modified
    local file_path = vim.fn.expand '%'

    if modified and modifiable and buftype == '' and file_path ~= '' then
      vim.cmd 'silent! write'
    end
  end,
})

-- Set 4-space indentation for C/C++ files
vim.api.nvim_create_autocmd('FileType', {
  pattern = { 'c', 'cpp' },
  callback = function()
    vim.opt_local.shiftwidth = 4
    vim.opt_local.tabstop = 4
    vim.opt_local.expandtab = true
    vim.opt_local.smartindent = true
  end,
})

-- Set 2-space indentation for Lua files (community standard)
vim.api.nvim_create_autocmd('FileType', {
  pattern = { 'lua' },
  callback = function()
    vim.opt_local.shiftwidth = 2
    vim.opt_local.tabstop = 2
    vim.opt_local.expandtab = true
    vim.opt_local.smartindent = true
  end,
})

